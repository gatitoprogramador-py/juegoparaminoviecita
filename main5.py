def parse_map(raw):
    HEX = {'A':10,'B':11,'C':12,'D':13,'E':14,'F':15}
    out = []
    for row in raw:
        r = []
        for c in row:
            if c == ' ':
                r.append(0)
            elif c in HEX:
                r.append(HEX[c])
            elif c.isdigit():
                r.append(int(c))
            else:
                r.append(0)
        out.append(r)
    return out

