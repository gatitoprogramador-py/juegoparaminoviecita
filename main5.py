_fog_surf = None

def draw_fog(surf, px_, py_, radius=None):
    global _fog_surf
    if radius is None:
        radius = VRAD * TILE
    if _fog_surf is None or _fog_surf.get_size() != surf.get_size():
        _fog_surf = pygame.Surface(surf.get_size(), pygame.SRCALPHA)
    _fog_surf.fill((0, 0, 0, 255))
    for i in range(radius + 12, 0, -2):
        alpha = min(255, int((i - radius) * 18)) if i > radius else 0
        pygame.draw.circle(_fog_surf, (0, 0, 0, alpha), (px_, py_), i)
    surf.blit(_fog_surf, (0, 0))


