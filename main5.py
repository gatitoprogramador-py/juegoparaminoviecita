class Player:
    MAX_HP = 5

    def __init__(self, x, y):
        self.x = float(x)
        self.y = float(y)
        self.facing = 'down'
        self.frame = 0
        self.anim_t = 0
        self.moving = False
        self.heartbeat_t = 0
        self.hp = 3
        self.invincible = 0
        # efectos
        self.dust = []

    def _collides(self, nx, ny, tm):
        hw = TILE // 3
        for fx, fy in [(nx - hw, ny + TILE//2 + 2),
                       (nx + hw, ny + TILE//2 + 2),
                       (nx - hw, ny + TILE - 2),
                       (nx + hw, ny + TILE - 2)]:
            if wall_at(tm, int(fx // TILE), int(fy // TILE)):
                return True
        return False

    def update(self, keys, tm):
        sp = 3 if keys[pygame.K_LSHIFT] else 2
        dx = dy = 0

        if keys[pygame.K_LEFT]  or keys[pygame.K_a]: dx = -sp; self.facing = 'left'
        if keys[pygame.K_RIGHT] or keys[pygame.K_d]: dx =  sp; self.facing = 'right'
        if keys[pygame.K_UP]    or keys[pygame.K_w]: dy = -sp; self.facing = 'up'
        if keys[pygame.K_DOWN]  or keys[pygame.K_s]: dy =  sp; self.facing = 'down'

        if dx and dy:
            dx = dx // abs(dx)
            dy = dy // abs(dy)

        nx, ny = self.x + dx, self.y + dy
        if not self._collides(nx, self.y, tm):
            self.x = nx
        if not self._collides(self.x, ny, tm):
            self.y = ny

        self.x = max(0, min(WW - TILE, self.x))
        self.y = max(0, min(WH - TILE, self.y))

        self.moving = bool(dx or dy)
        if self.moving:
            self.anim_t += 1
            if self.anim_t >= 8:
                self.anim_t = 0
                self.frame = (self.frame + 1) % 4
            # polvo de pasos
            if self.anim_t == 0 and random.random() < 0.4:
                self.dust.append({
                    'x': self.x + TILE//2 + random.randint(-4, 4),
                    'y': self.y + TILE - 2,
                    'life': 8,
                    'vx': random.uniform(-0.4, 0.4),
                })
        else:
            self.frame = 0
            self.anim_t = 0

        self.heartbeat_t += 1
        if self.invincible > 0:
            self.invincible -= 1

        self.dust = [d for d in self.dust if d['life'] > 0]
        for d in self.dust:
            d['x'] += d['vx']
            d['y'] -= 0.3
            d['life'] -= 1

    def draw(self, surf, cam, cache):
        spr = cache[f'g_{self.facing}_{self.frame}']
        sw, sh = spr.get_size()
        sx, sy = cam.ap(int(self.x) + TILE//2 - sw//2, int(self.y) + TILE - sh + 2)

        # parpadeo de invencibilidad
        if self.invincible > 0 and (self.invincible // 4) % 2 == 1:
            return

        # sombra
        shadow = pygame.Surface((sw, 4), pygame.SRCALPHA)
        pygame.draw.ellipse(shadow, (0, 0, 0, 60), shadow.get_rect())
        surf.blit(shadow, (sx, sy + sh - 2))

        surf.blit(spr, (sx, sy))

        # corazón flotante sobre la cabeza
        hk = 'h_bri' if (self.heartbeat_t // 16) % 2 == 0 else 'h_red'
        hs = cache[hk]
        hx = sx + sw // 2 - hs.get_width() // 2
        hy = sy - hs.get_height() - 2 + int(math.sin(self.heartbeat_t * 0.1) * 1.5)
        surf.blit(hs, (hx, hy))

        # polvo
        for d in self.dust:
            dsx, dsy = cam.ap(int(d['x']), int(d['y']))
            alpha = int(200 * d['life'] / 8)
            col = (alpha, alpha, alpha)
            pygame.draw.circle(surf, col, (dsx, dsy), 1)


