def tutorial_screen(screen, canvas, clock, cache):
    music_load('tutorial')
    page = 0
    total = len(TUTORIAL_PAGES)
    t = 0
    transition = 0.0  # 0..1 fade in
    stars = [(random.randint(0, VW - 1), random.randint(0, VH - 1),
              random.randint(0, 2)) for _ in range(80)]

    while True:
        for ev in pygame.event.get():
            if ev.type == pygame.QUIT:
                pygame.quit(); sys.exit()
            if ev.type == pygame.KEYDOWN:
                if ev.key == pygame.K_ESCAPE:
                    return
                if ev.key in (pygame.K_z, pygame.K_RETURN, pygame.K_RIGHT, pygame.K_d):
                    if page < total - 1:
                        page += 1
                        transition = 0.0
                    else:
                        return
                if ev.key in (pygame.K_LEFT, pygame.K_a) and page > 0:
                    page -= 1
                    transition = 0.0

        t += 1
        transition = min(1.0, transition + 0.06)
        canvas.fill(BG)

        # estrellas
        for sx, sy, br in stars:
            if (t + sx * 3 + sy) % 50 < 25:
                c = (80, 80, 80) if br else (35, 35, 35)
                canvas.set_at((sx, sy), c)

        # corazón decorativo
        beat = 1.0 + math.sin(t * 0.07) * 0.08
        hs = cache['h_bri']
        hw, hh = hs.get_size()
        big = pygame.transform.scale(hs, (int(hw * beat * 3), int(hh * beat * 3)))
        blit_c(canvas, big, VW // 2, 28)

        # título de página
        p = TUTORIAL_PAGES[page]
        fade = int(255 * transition)
        title_s = F14.render(p['title'], True, (min(255, GOLD[0]),
                                                min(255, GOLD[1]),
                                                min(255, GOLD[2])))
        title_s.set_alpha(fade)
        canvas.blit(title_s, (VW // 2 - title_s.get_width() // 2, 50))
        pygame.draw.line(canvas, GOLD, (VW // 4, 64), (3 * VW // 4, 64), 1)

        # contenido
        for i, line in enumerate(p['lines']):
            if not line:
                continue
            s = F6.render(line, True, (min(255, WHITE[0]),) * 3)
            s.set_alpha(fade)
            canvas.blit(s, (VW // 2 - s.get_width() // 2, 72 + i * 12))

        # hints de teclas
        yh = VH - 35
        pygame.draw.line(canvas, DGRAY, (10, yh - 2), (VW - 10, yh - 2), 1)
        for i, hint in enumerate(p['key_hints']):
            hs2 = F6.render(hint, True, (100, 160, 255))
            canvas.blit(hs2, (VW // 2 - hs2.get_width() // 2, yh + i * 9))

        # paginación
        pg_txt = F6.render(f'{page + 1} / {total}', True, GRAY)
        canvas.blit(pg_txt, (VW // 2 - pg_txt.get_width() // 2, VH - 10))

        nav_l = F6.render('◄', True, GRAY if page > 0 else DGRAY)
        nav_r = F6.render('►', True, GRAY if page < total - 1 else DGRAY)
        canvas.blit(nav_l, (10, VH - 10))
        canvas.blit(nav_r, (VW - 15, VH - 10))

        pygame.transform.scale(canvas, (SW, SH), screen)
        pygame.display.flip()
        clock.tick(FPS)


