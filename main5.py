def px(mat, pal, sz=1):
    """Convierte una matriz de índices + paleta → Surface."""
    if not mat:
        return pygame.Surface((1, 1), pygame.SRCALPHA)
    max_c = max(len(r) for r in mat)
    s = pygame.Surface((max_c * sz, len(mat) * sz), pygame.SRCALPHA)
    for ri, row in enumerate(mat):
        for ci, v in enumerate(row):
            col = pal.get(v)
            if col:
                pygame.draw.rect(s, col, (ci * sz, ri * sz, sz, sz))
    return s

def tile_surf(mat, pal, tw=TILE, th=TILE):
    s = pygame.Surface((tw, th))
    for ri, row in enumerate(mat):
        for ci, v in enumerate(row):
            s.set_at((ci, ri), pal.get(v, BG))
    return s

def blit_c(surf, spr, x, y):
    surf.blit(spr, (x - spr.get_width() // 2, y - spr.get_height() // 2))

def draw_heart(surf, x, y, col, s=5):
    r = max(1, s // 2)
    pygame.draw.circle(surf, col, (x - r, y - 1), r)
    pygame.draw.circle(surf, col, (x + r, y - 1), r)
    pts = [(x - s, y + 2), (x + s, y + 2), (x, y + s + 3)]
    pygame.draw.polygon(surf, col, pts)

def wrap_text(text, font, max_w):
    words = text.split()
    lines, cur = [], ''
    for w in words:
        test = cur + w + ' '
        if font.size(test)[0] > max_w and cur:
            lines.append(cur.rstrip())
            cur = w + ' '
        else:
            cur = test
    if cur:
        lines.append(cur.rstrip())
    return lines

def mirrorH(m):
    return [list(reversed(r)) for r in m]

def lerp_color(a, b, t):
    return tuple(int(a[i] + (b[i] - a[i]) * t) for i in range(3))

def gradient_rect(surf, c1, c2, rect, vertical=True):
    for i in range(rect.height if vertical else rect.width):
        t = i / max(1, (rect.height - 1) if vertical else (rect.width - 1))
        c = lerp_color(c1, c2, t)
        if vertical:
            pygame.draw.line(surf, c, (rect.x, rect.y + i), (rect.x + rect.width - 1, rect.y + i))
        else:
            pygame.draw.line(surf, c, (rect.x + i, rect.y), (rect.x + i, rect.y + rect.height - 1))

