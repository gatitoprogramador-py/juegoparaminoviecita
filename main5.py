def title_screen(screen, canvas, clock, cache):
    music_load('title')
    stars = [(random.randint(0, VW - 1), random.randint(0, VH - 1),
              random.uniform(0.3, 1.0)) for _ in range(120)]
    shooting_stars = []
    t = 0
    ms = MusicSelector()

    while True:
        for ev in pygame.event.get():
            if ev.type == pygame.QUIT:
                pygame.quit(); sys.exit()
            if ev.type == pygame.KEYDOWN:
                if ev.key in (pygame.K_RETURN, pygame.K_SPACE, pygame.K_z):
                    return
                if ev.key == pygame.K_m:
                    ms.show()
                if ev.key == pygame.K_t:
                    tutorial_screen(screen, canvas, clock, cache)
            if ms.active:
                ms.handle(ev)

        t += 1
        # estrellas fugaces aleatorias
        if random.random() < 0.015:
            shooting_stars.append({
                'x': float(random.randint(VW // 4, VW - 20)),
                'y': float(random.randint(0, VH // 3)),
                'vx': random.uniform(-3, -1),
                'vy': random.uniform(1, 3),
                'life': random.randint(18, 35),
                'trail': [],
            })
        for ss in shooting_stars:
            ss['trail'].append((ss['x'], ss['y']))
            if len(ss['trail']) > 6:
                ss['trail'].pop(0)
            ss['x'] += ss['vx']
            ss['y'] += ss['vy']
            ss['life'] -= 1
        shooting_stars = [ss for ss in shooting_stars if ss['life'] > 0]

        if ms.active:
            ms.draw(canvas)
            pygame.transform.scale(canvas, (SW, SH), screen)
            pygame.display.flip()
            clock.tick(FPS)
            continue

        canvas.fill(BG)

        # fondo estrellado con brillo
        for sx, sy, br in stars:
            if (t + int(sx * 2 + sy)) % int(40 / br) < int(20 / br):
                lum = int(80 * br)
                canvas.set_at((sx, sy), (lum, lum, lum))

        # estrellas fugaces
        for ss in shooting_stars:
            for i, (tx, ty) in enumerate(ss['trail']):
                alpha_f = (i + 1) / len(ss['trail'])
                c = int(200 * alpha_f)
                pygame.draw.circle(canvas, (c, c, c), (int(tx), int(ty)), 1)

        # corazón central pulsante
        pulse = 1.0 + math.sin(t * 0.04) * 0.15
        hs = cache['h_bri']
        hw, hh = hs.get_size()
        big = pygame.transform.scale(hs, (int(hw * pulse * 5), int(hh * pulse * 5)))
        blit_c(canvas, big, VW // 2, VH // 2 - 55)

        # halo alrededor del corazón
        halo = pygame.Surface((120, 120), pygame.SRCALPHA)
        pygame.draw.circle(halo, (180, 20, 20, int(25 * pulse)), (60, 60), int(50 * pulse))
        canvas.blit(halo, (VW // 2 - 60, VH // 2 - 55 - 60))

        # título con sombra
        def blit_txt_shadow(txt, font, col, shadow_col, cx, cy):
            sh = font.render(txt, True, shadow_col)
            s = font.render(txt, True, col)
            canvas.blit(sh, (cx - sh.get_width() // 2 + 2, cy + 2))
            canvas.blit(s, (cx - s.get_width() // 2, cy))

        blit_txt_shadow('ALMA DE PAPEL', F20, WHITE, (40, 40, 40), VW // 2, VH // 2 - 20)

        sub = F8.render('un corazón que lucha para salvar al que ama', True, (100, 100, 115))
        canvas.blit(sub, (VW // 2 - sub.get_width() // 2, VH // 2 - 4))

        if (t // 24) % 2 == 0:
            p = F8.render('[ ENTER / Z  para comenzar ]', True, (160, 160, 175))
            canvas.blit(p, (VW // 2 - p.get_width() // 2, VH // 2 + 15))

        # hints
        h1 = F6.render('[ T ] tutorial', True, (55, 55, 65))
        h2 = F6.render('[ M ] selector de música', True, (55, 55, 65))
        canvas.blit(h1, (VW // 2 - h1.get_width() // 2, VH - 20))
        canvas.blit(h2, (VW // 2 - h2.get_width() // 2, VH - 11))

        # versión
        v = F6.render('v4.0', True, (35, 35, 45))
        canvas.blit(v, (VW - v.get_width() - 3, 3))

        pygame.transform.scale(canvas, (SW, SH), screen)
        pygame.display.flip()
        clock.tick(FPS)


