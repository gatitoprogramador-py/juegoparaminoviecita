class Cam:
    def __init__(self):
        self.x = self.y = 0.0
        self.shake_x = self.shake_y = 0
        self.shake_dur = 0

    def shake(self, intensity=4, dur=18):
        self._shake_int = intensity
        self.shake_dur = dur

    def update(self, tx, ty):
        cx = max(0, min(WW - VW, tx - VW // 2))
        cy = max(0, min(WH - VH, ty - VH // 2))
        self.x += (cx - self.x) * 0.14
        self.y += (cy - self.y) * 0.14
        if self.shake_dur > 0:
            self.shake_dur -= 1
            i = getattr(self, '_shake_int', 3)
            self.shake_x = random.randint(-i, i)
            self.shake_y = random.randint(-i, i)
        else:
            self.shake_x = self.shake_y = 0

    def ap(self, wx, wy):
        return int(wx - self.x) + self.shake_x, int(wy - self.y) + self.shake_y


