class GM:
    def __init__(self, cache):
        self.cache = cache
        self.room = 0
        self.tm = MAPS[0]
        self.world = World(self.tm)
        self.cam = Cam()
        self.player = Player(TILE * 2, TILE * 14)
        self.battle = Battle(cache)
        self.dlg = Dialog()
        self.cookie_house = CookieHouse()
        self.music_sel = MusicSelector()
        self.state = 'explore'
        self.souls = [False] * 8
        self.done = set()
        self.pending_btl = None
        self.pending_dlg = None
        self.story = {k: False for k in [
            'dado_intro', 'phase2', 'cookie_met',
            'cookie_corrupt', 'king_done', 'ultra_done',
        ]}
        self.exit_cooldown = 0
        self.t = 0
        # efectos de transición de sala
        self.transition_alpha = 0
        self.transition_dir = 0   # 0=nada, 1=fade in, -1=fade out
        music_load('room0')

    def go_room(self, idx):
        self.room = idx
        self.tm = MAPS[idx]
        self.world = World(self.tm)
        self.player.x = float(TILE * 20)
        self.player.y = float(TILE * 14)
        self.cam.x = float(TILE * 20 - VW // 2)
        self.cam.y = float(TILE * 14 - VH // 2)
        self.exit_cooldown = 90
        self.transition_alpha = 255
        self.transition_dir = 1   # fade in

        slots = ['room0','room1','room2','room3','room4',
                 'room5','room6','room7','cookie','boss']
        music_load(slots[idx] if idx < len(slots) else 'room0')

    def check_exit(self):
        if self.exit_cooldown > 0:
            return
        tc = int(self.player.x // TILE)
        tr = int(self.player.y // TILE)
        if 0 <= tr < ROWS and 0 <= tc < COLS and self.tm[tr][tc] == 3:
            if self.room < len(MAPS) - 1:
                self.go_room(self.room + 1)

    def near_npc(self):
        px_ = int(self.player.x)
        py_ = int(self.player.y)
        for dr in range(-2, 3):
            for dc in range(-2, 3):
                tr = py_ // TILE + dr
                tc = px_ // TILE + dc
                if 0 <= tr < ROWS and 0 <= tc < COLS:
                    v = self.tm[tr][tc]
                    if v in (5, 6, 7, 8):
                        return v, tr, tc
        return None, 0, 0

    def trigger_z(self):
        v, tr, tc = self.near_npc()
        k = f'{tr}_{tc}'
        if v == 5 and k not in self.done:
            self.done.add(k)
            self.state = 'dialog'
            self.dlg.start(DUCK_PRE)
            self.pending_btl = ('PATO', self.cache['duck'], 22, True, 'duck')
        elif v == 6 and k not in self.done:
            self.done.add(k)
            self.state = 'dialog'
            self.dlg.start(FROG_PRE)
            self.pending_btl = ('RANA', self.cache['frog'], 20, True, 'frog')
        elif v == 7 and not self.story['cookie_met']:
            self.story['cookie_met'] = True
            self.state = 'dialog'
            self.dlg.start(COOKIE_MEET)
            self.pending_dlg = 'cookie_house'
        elif v == 7 and self.story['cookie_met'] and not self.story['cookie_corrupt']:
            self.state = 'cookie_house'
            self.cookie_house.show('¡Bienvenida de nuevo, mi niña!~')
        elif v == 8 and k not in self.done:
            self.done.add(k)
            self.state = 'dialog'
            self.dlg.start(RAT_MEET)
            self.pending_dlg = PIG_REVEAL
            self.pending_btl = ('CERDO', self.cache['pig'], 16, False, 'pig')

    def after_dialog(self):
        if self.pending_dlg == 'cookie_house':
            self.pending_dlg = None
            self.state = 'cookie_house'
            self.cookie_house.show()
        elif isinstance(self.pending_dlg, list):
            lines = self.pending_dlg
            self.pending_dlg = None
            if self.pending_btl:
                name, surf, hp, fg, eid = self.pending_btl
                self.pending_btl = None
                music_load('boss')
                self.battle.start(name, surf, hp, fg)
                self.state = 'battle'
            else:
                self.state = 'dialog'
                self.dlg.start(lines)
        elif self.pending_btl:
            name, surf, hp, fg, eid = self.pending_btl
            self.pending_btl = None
            music_load('boss')
            self.battle.start(name, surf, hp, fg)
            self.state = 'battle'
        else:
            self.state = 'explore'
            music_load(f'room{min(self.room, 9)}')

    def after_battle(self):
        n = self.battle.ename
        o = self.battle.outcome
        slot = f'room{min(self.room, 9)}'

        if n == 'PATO':
            self.souls[0] = True
            after = DUCK_WIN if o == 'forgive' else DUCK_DEAD
        elif n == 'RANA':
            self.souls[1] = True
            after = FROG_WIN if o == 'forgive' else FROG_DEAD
        elif n == 'CERDO':
            self.souls[2] = True
            after = PIG_DEAD
            self.pending_dlg = RAT_WIN if o == 'forgive' else RAT_DEAD
        elif n == 'OSO':
            self.souls[3] = True
            after = BEAR_WIN if o == 'forgive' else BEAR_DEAD
        elif n == 'MAMÁ COOKIE':
            self.souls[4] = True
            after = COOKIE_WIN if o == 'forgive' else COOKIE_DEAD
        elif n == 'REY GATO':
            self.souls[5] = True
            after = KING_WIN
        elif n == 'EL DADO' and not self.story['phase2']:
            self.story['phase2'] = True
            self.state = 'dialog'
