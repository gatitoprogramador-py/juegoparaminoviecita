def music_load(slot_name: str, volume: float = 0.55):
    global _current_track
    if not pygame.mixer.get_init():
        return
    for ext in ['.mp3', '.ogg', '.wav', '.flac']:
        p = MUSIC_DIR / (slot_name + ext)
        if p.exists():
            key = str(p)
            if _current_track == key:
                return
            try:
                pygame.mixer.music.load(key)
                pygame.mixer.music.set_volume(volume)
                pygame.mixer.music.play(-1)
                _current_track = key
                return
            except Exception:
                pass
    sentinel = slot_name + '_none'
    if _current_track != sentinel:
        try:
            pygame.mixer.music.stop()
        except Exception:
            pass
        _current_track = sentinel

def music_stop():
    global _current_track
    try:
        pygame.mixer.music.stop()
    except Exception:
        pass
    _current_track = None

