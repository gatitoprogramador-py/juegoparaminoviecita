def main():
    cache = build_cache()
    gm = GM(cache)

    # Pantalla título
    title_screen(screen, canvas, clock, cache)
    music_load('room0')

    # Diálogo de intro
    gm.state = 'dialog'
    gm.dlg.start(INTRO_DLG)

    z_prev = False
    m_prev = False
    running = True

    while running:
        clock.tick(FPS)
        keys = pygame.key.get_pressed()

        z_now = keys[pygame.K_z] or keys[pygame.K_RETURN]
        z_pressed = z_now and not z_prev
        z_prev = z_now

        m_now = keys[pygame.K_m]
        m_pressed = m_now and not m_prev
        m_prev = m_now

        for ev in pygame.event.get():
            if ev.type == pygame.QUIT:
                running = False

            if ev.type == pygame.KEYDOWN:
                if ev.key == pygame.K_ESCAPE:
                    running = False

                # T en exploración → tutorial
                if ev.key == pygame.K_t and gm.state == 'explore':
                    tutorial_screen(screen, canvas, clock, cache)
                    music_load(f'room{min(gm.room, 9)}')

            # enrutar eventos según estado
            if gm.state == 'dialog':
                gm.dlg.handle(ev)
            elif gm.state == 'battle':
                gm.battle.handle(ev)
            elif gm.state == 'cookie_house':
                gm.cookie_house.handle(ev, gm.player)
            elif gm.state == 'music_sel':
                gm.music_sel.handle(ev)
            elif gm.state == 'hearts':
                if ev.type == pygame.KEYDOWN and ev.key in (pygame.K_z, pygame.K_RETURN):
                    music_load('boss2')
                    gm.battle.start('EL SOL', cache['sun'], 50, False)
                    gm.state = 'battle'

        # game over check
        if gm.player.hp <= 0 and gm.state in ('explore', 'dialog'):
            retry = gameover_screen(screen, canvas, clock, cache)
            if retry:
                gm.player.hp = 3
                music_load(f'room{min(gm.room, 9)}')
            else:
                running = False
                continue

        gm.update(keys, z_pressed, m_pressed)
        gm.draw(canvas)
        pygame.transform.scale(canvas, (SW, SH), screen)
        pygame.display.flip()

    # Secuencia final
    ending_screen(screen, canvas, clock, cache, gm.souls)
    letter_screen(screen, canvas, clock)
    pygame.quit()
    sys.exit()


if __name__ == '__main__':
    main()