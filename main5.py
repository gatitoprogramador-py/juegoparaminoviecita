def ending_screen(screen, canvas, clock, cache, souls):
    music_load('ending')
    t = 0
    dlg = Dialog()
    dlg.start(ENDING_DLG)
    done_dlg = False

    while True:
        for ev in pygame.event.get():
            if ev.type == pygame.QUIT:
                pygame.quit(); sys.exit()
            if ev.type == pygame.KEYDOWN:
                if ev.key == pygame.K_ESCAPE:
                    return
                dlg.handle(ev)
                if dlg.finished:
                    done_dlg = True

        t += 1
        dlg.update()

        dawn = min(1.0, t / 420)
        sky1 = lerp_color((0, 0, 0), (255, 175, 95), dawn)
        sky2 = lerp_color((0, 0, 0), (255, 135, 55), dawn)

        for y in range(VH):
            r2 = y / VH
            c = lerp_color(sky1, sky2, r2)
            pygame.draw.line(canvas, c, (0, y), (VW, y))

        # sol
        if t > 60:
            sr = min(45, int((t - 60) * 0.12))
            pygame.draw.circle(canvas, (255, 215, 75), (VW // 2, VH // 2 - 25), sr)
            pygame.draw.circle(canvas, (255, 248, 160), (VW // 2, VH // 2 - 25), max(1, sr - 5))

        # almas orbitando
        for i, has in enumerate(souls):
            if has:
                ang = t * 0.022 + i * (math.tau / 8)
                hx_ = int(VW // 2 + math.cos(ang) * 72)
                hy_ = int(VH // 2 + math.sin(ang) * 38)
                draw_heart(canvas, hx_, hy_, SOUL_COLORS[i], 4)
                # nombre al pasar por abajo
                if math.sin(ang) > 0.85:
                    sn = F6.render(SOUL_NAMES[i], True, SOUL_COLORS[i])
                    canvas.blit(sn, (VW // 2 - sn.get_width() // 2, VH // 2 + 50))

        if not done_dlg:
            dlg.draw(canvas)
        else:
            if (t // 28) % 2 == 0:
                msg = F6.render('[ Z ] ver carta', True, (195, 195, 195))
                canvas.blit(msg, (VW // 2 - msg.get_width() // 2, VH - 13))
            if pygame.key.get_pressed()[pygame.K_z] or pygame.key.get_pressed()[pygame.K_RETURN]:
                return

        pygame.transform.scale(canvas, (SW, SH), screen)
        pygame.display.flip()
        clock.tick(FPS)


