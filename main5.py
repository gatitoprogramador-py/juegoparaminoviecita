    def _load(self):
        if self.idx >= len(self.lines):
            self.active = False
            self.finished = True
            return
        row = self.lines[self.idx]
        self.spk = row[0]
        self.txt = row[1]
        self.opts = row[2] if len(row) == 3 else None
        self.ci = 0.0
        self.done = False
        self.sel = 0
        # personalizar borde por hablante
        self.border_col = self.NAME_COLORS.get(self.spk, WHITE)

    def handle(self, ev):
        if not self.active:
            return
        if ev.type == pygame.KEYDOWN:
            if self.opts and self.done:
                if ev.key in (pygame.K_LEFT, pygame.K_a):
                    self.sel = max(0, self.sel - 1)
                if ev.key in (pygame.K_RIGHT, pygame.K_d):
                    self.sel = min(len(self.opts) - 1, self.sel + 1)
                if ev.key in (pygame.K_z, pygame.K_RETURN):
                    self.result = self.opts[self.sel]
                    self.idx += 1
                    self._load()
            elif ev.key in (pygame.K_z, pygame.K_RETURN):
                if not self.done:
                    self.ci = float(len(self.txt))
                    self.done = True
                else:
                    self.idx += 1
                    self._load()

