def gameover_screen(screen, canvas, clock, cache):
    music_load('gameover')
    t = 0
    while True:
        for ev in pygame.event.get():
            if ev.type == pygame.QUIT:
                pygame.quit(); sys.exit()
            if ev.type == pygame.KEYDOWN and t > 60:
                if ev.key in (pygame.K_z, pygame.K_RETURN):
                    return True   # retry
                if ev.key == pygame.K_ESCAPE:
                    return False  # quit

        t += 1
        canvas.fill(BG)
        fade = min(1.0, t / 60)

        # texto
        go = F20.render('GAME  OVER', True, (int(220 * fade), 0, 0))
        canvas.blit(go, (VW // 2 - go.get_width() // 2, VH // 2 - 20))

        if t > 80:
            s1 = F8.render('[Z] Intentar de nuevo', True, (160, 160, 160))
            s2 = F8.render('[ESC] Salir', True, (100, 100, 100))
            canvas.blit(s1, (VW // 2 - s1.get_width() // 2, VH // 2 + 10))
            canvas.blit(s2, (VW // 2 - s2.get_width() // 2, VH // 2 + 22))

        # corazón roto
        hd = cache['h_dim']
        pulse = 0.8 + math.sin(t * 0.08) * 0.1
        bigs = pygame.transform.scale(hd, (int(hd.get_width() * pulse * 4),
                                           int(hd.get_height() * pulse * 4)))
        blit_c(canvas, bigs, VW // 2, VH // 2 - 45)

        pygame.transform.scale(canvas, (SW, SH), screen)
        pygame.display.flip()
        clock.tick(FPS)


